<?php
// Создаем массив с целыми числами
$numbers = [3, 7, 11, 5, 9];

// Инициализируем переменную для хранения суммы элементов массива
$sum = 0;

// Находим сумму всех элементов массива
foreach ($numbers as $number) {
    $sum += $number;
}

// Выводим результат
echo "Сумма всех элементов массива: " . $sum;
?>




